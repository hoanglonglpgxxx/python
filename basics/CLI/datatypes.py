# Numeric Types
integer_value = 10  # int
float_value = 10.5  # float
complex_value = 1 + 2j  # complex

# Sequence Types
string_value = "Hello, World!"  # str
list_value = [1, 2, 3, 4, 5]  # list
tuple_value = (1, 2, 3, 4, 5)  # tuple
range_value = range(5)  # range

# Mapping Type
dict_value = {"key1": "value1", "key2": "value2"}  # dict

# Set Types
set_value = {1, 2, 3, 4, 5}  # set
frozenset_value = frozenset([1, 2, 3, 4, 5])  # frozenset

# Boolean Type
boolean_value = True  # bool

# Binary Types
bytes_value = b"Hello"  # bytes
bytearray_value = bytearray(b"Hello")  # bytearray
memoryview_value = memoryview(bytes_value)  # memoryview

# None Type
none_value = None  # NoneType

# Print all values and their types
print(f"integer_value: {integer_value}, type: {type(integer_value)}")
print(f"float_value: {float_value}, type: {type(float_value)}")
print(f"complex_value: {complex_value}, type: {type(complex_value)}")
print(f"string_value: {string_value}, type: {type(string_value)}")
print(f"list_value: {list_value}, type: {type(list_value)}")
print(f"tuple_value: {tuple_value}, type: {type(tuple_value)}")
print(f"range_value: {range_value}, type: {type(range_value)}")
print(f"dict_value: {dict_value}, type: {type(dict_value)}")
print(f"set_value: {set_value}, type: {type(set_value)}")
print(f"frozenset_value: {frozenset_value}, type: {type(frozenset_value)}")
print(f"boolean_value: {boolean_value}, type: {type(boolean_value)}")
print(f"bytes_value: {bytes_value}, type: {type(bytes_value)}")
print(f"bytearray_value: {bytearray_value}, type: {type(bytearray_value)}")
print(f"memoryview_value: {memoryview_value}, type: {type(memoryview_value)}")
print(f"none_value: {none_value}, type: {type(none_value)}")